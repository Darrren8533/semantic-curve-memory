BASE_TEXT = ''.join(TEXTS)

# 生成不同长度的文本
def make_text(target_len):
    t = (BASE_TEXT * (target_len // len(BASE_TEXT) + 2))[:target_len]
    return t

# ─── 核心函数 ─────────────────────────────────────

def build_embeddings(text, window=4, dim=8):
    unique = list(dict.fromkeys(text))
    idx    = {c: i for i, c in enumerate(unique)}
    n      = len(unique)
    cooc   = np.zeros((n, n))
    for pos, char in enumerate(text):
        i = idx[char]
        for delta in range(-window, window+1):
            if delta and 0 <= pos+delta < len(text):
                cooc[i, idx[text[pos+delta]]] += 1
    U, S, _ = np.linalg.svd(cooc, full_matrices=False)
    embs    = U[:,:dim] * S[:dim]
    return unique, idx, embs

def fourier_fit(x, Y, K):
    T   = float(x[-1]) if x[-1] != 0 else 1.0
    Phi = np.zeros((len(x), 2*K+1))
    Phi[:,0] = 1.0
    for k in range(1, K+1):
        Phi[:,2*k-1] = np.cos(2*np.pi*k*x/T)
        Phi[:,2*k  ] = np.sin(2*np.pi*k*x/T)
    coeffs, _, _, _ = np.linalg.lstsq(Phi, Y, rcond=None)
    return coeffs, T

def fourier_eval_batch(x_arr, coeffs, T, K):
    Phi = np.zeros((len(x_arr), 2*K+1))
    Phi[:,0] = 1.0
    for k in range(1, K+1):
        Phi[:,2*k-1] = np.cos(2*np.pi*k*x_arr/T)
        Phi[:,2*k  ] = np.sin(2*np.pi*k*x_arr/T)
    return Phi @ coeffs

def run_test(text, K=50, dim=8):
    N = len(text)
    unique, idx, all_embs = build_embeddings(text, dim=dim)
    seq_embs = np.array([all_embs[idx[c]] for c in text])
    x        = np.arange(N, dtype=float)

    coeffs, T  = fourier_fit(x, seq_embs, K)
    restored   = fourier_eval_batch(x, coeffs, T, K)
    dists      = cdist(restored, all_embs, metric='euclidean')
    best_chars = [unique[i] for i in np.argmin(dists, axis=1)]
    accuracy   = sum(a==b for a,b in zip(text, best_chars)) / N

    formula_sz = (2*K+1) * dim
    return N, formula_sz, accuracy

